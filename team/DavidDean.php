			<h3>David Dean, Ph.D.</h3>
			<p> David Dean received his Ph.D. in Biological Anthropology at the City University of New York in 1993. 
			He completed a post-doctoral fellowship in the Institute of Reconstructive Plastic Surgery in 1994 at the New York University School of Medicine before joining the faculty of the School of Medicine at Case Western Reserve University in Cleveland. 
			He directs the Department of Neurological Surgery Imaging Laboratory. His research interests are in Computer-Assisted Surgery, skull (bone) tissue engineering, photodynamic therapy of glioma, 
			and automated radiosurgery treatment planning.</p>

